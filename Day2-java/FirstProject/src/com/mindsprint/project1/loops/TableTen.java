package com.mindsprint.project1.loops;

public class TableTen {
    public static void main(String[] args ){
        int num=10;
        System.out.println("Multiplication table of "+num+":");
        for(int i=1;i<=10;i++)
        {
            System.out.println(num+"x" +i +"="+(num*i));
        }

    }
}
